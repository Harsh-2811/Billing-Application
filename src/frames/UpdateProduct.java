    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

       Product p = new Product();
       p.setName(jTextField2.getText());
       p.setPrice(Double.parseDouble(jTextField3.getText()));
       p.setDescription(jTextArea1.getText());
       p.setCategory((String) jComboBox1.getSelectedItem());
       
       ProductDao pdao= new ProductDao();
       if( pdao.updateProduct(p,Integer.parseInt(jTextField1.getText()) ))
       {
           JOptionPane.showMessageDialog(null, "Product Successfully Updated" ,"Result", JOptionPane.INFORMATION_MESSAGE);
       }
       else{
        JOptionPane.showMessageDialog(null, "Something Went Wrong" ,"Result", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_jButton2ActionPerformed

