    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ProductDao pdao= new ProductDao();
        Product p =pdao.getProduct(Integer.parseInt(jTextField1.getText()));
        if(p == null)
        {
            JOptionPane.showMessageDialog(null, "No Record for id "+jTextField1.getText(), "Result says", JOptionPane.ERROR_MESSAGE);
        }
        else{
            jTextField2.setText(p.getName());
            jTextField3.setText(Double.toString(p.getPrice()));
            jTextArea1.setText(p.getDescription());
            jComboBox1.setSelectedItem((String)p.getCategory());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

